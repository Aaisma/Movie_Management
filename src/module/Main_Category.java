package module;

public class Main_Category {
        String Sofa;
        String Seat;

    public Main_Category(String sofa, String seat) {
        Sofa = sofa;
        Seat = seat;
    }

    public String getSofa() {
        return Sofa;
    }

    public void setSofa(String sofa) {
        Sofa = sofa;
    }

    public String getSeat() {
        return Seat;
    }

    public void setSeat(String seat) {
        Seat = seat;
    }
}
